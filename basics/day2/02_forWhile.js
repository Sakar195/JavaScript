// Using for to execute a block of statement in a loop

let numbers = [1, 2, 3, 4, 5];

for (let i = 0; i < numbers.length; i++) {
    console.log(numbers[i]); // Output: 1 2 3 4 5 (each on a new line)
}




//while loop example
let count = 1;

while (count <= 5) {
    console.log(count);
    count++;
}

console.log('Counting completed!');